//isStart는 최초false로 저장 maybeStart함수가 처음 실행되는 경우 true
//maybeStart는 createPeerConnection 함수
//peerconnection을 만들어주고, 나의 peerconnection에 localStream붙임
//isInitiator인 경우(방을 만들면) doCall 함수를 통해 같은 방에 있는 client에게 rtc요청
function maybeStart() {
  console.log('>>>>>>> maybeStart() ', isStarted, localStream, isChannelReady);
  if (!isStarted && typeof localStream !== 'undefined' && isChannelReady) {
    console.log('>>>>>> creating peer connection');
    createPeerConnection();
    pc.addStream(localStream);
    isStarted = true;
    console.log('isInitiator', isInitiator);
    if (isInitiator) {
      doCall();
    }
  }
}

